#include <bits/stdc++.h>

using namespace std;

class Solution {
public:
    bool isValidSudoku(vector<vector<char>>& board) {
        for (int i = 0; i < 9; ++i) {
            int a1[9] { 0 }, a2[9] { 0 }, a3[9] { 0 };
            for (int j = 0; j < 9; ++j) {
                char c1 = board[i][j];
                char c2 = board[j][i];
                char c3 = board[i / 3 * 3 + j / 3][i % 3 * 3 + j % 3];
                if (c1 != '.' && ++a1[c1 - '1'] > 1) return false;
                if (c2 != '.' && ++a2[c2 - '1'] > 1) return false;
                if (c3 != '.' && ++a3[c3 - '1'] > 1) return false;
            }
        }
        return true;
    }
};

