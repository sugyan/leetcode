class Solution {
public:
    vector<double> sampleStats(vector<int>& count) {
        double minimum = 255, maximum = 0;
        vector<double> answer {
            256.0, 0.0, 0.0, 0.0, 0.0
        };
        long sum = 0, m = 0, modeindex = 0;
        long sampled = 0;
        for (int i = 0; i < 256; ++i) {
            if (answer[0] > 255 && count[i] > 0) answer[0] = double(i);
            if (answer[1] == 0 && count[255 - i] > 0) answer[1] = double(255 - i);
            if (count[i] > m) {
                m = count[i];
                modeindex = i;
            }
            sum += i * count[i];
            sampled += count[i];
        }
        answer[2] = 1.0 * sum / sampled;
        answer[4] = double(modeindex);
        long sampled2 = 0;
        for (int i = 0; i < 256; ++i) {
            sampled2 += count[i];
            if (sampled2 >= sampled / 2) {
                answer[3] = double(i);
                if (sampled % 2 == 0 && sampled2 * 2 == sampled) {
                    answer[3] = 0.5 * (i + i + 1);
                }
                break;
            }
        }
        return answer;
    }
};

